package com.javaOperators;

public class AssignmentOpeartors {
//Assignment operator is also called as Short-hand operator.
	public static void main(String[] args) {
		int num1=10;
		System.out.println(num1 +=22);
		System.out.println(num1 -=22);
		System.out.println(num1 /=22);
		System.out.println(num1 *=22);
		System.out.println(num1 %=22);
	}
}
